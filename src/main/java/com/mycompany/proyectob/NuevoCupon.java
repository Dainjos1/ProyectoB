    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        JFileChooser fileChooser = new JFileChooser();
        int resultado = fileChooser.showOpenDialog(this);
        
        if (resultado == JFileChooser.APPROVE_OPTION){
        File archivo = fileChooser.getSelectedFile();
        try(BufferedReader br = new BufferedReader(new FileReader(archivo))){
            String linea;
            boolean primeraLinea = true;
        
            modeloTabla.setRowCount(0);
            CargaMasiva.clear();
            
            while ((linea = br.readLine()) != null) {
                String[] partes = linea.split("\\|");

                if (primeraLinea) {
                    primeraLinea = false;
                    continue;
                }

                if (partes.length != 4) continue;

                String codigo = partes[0].trim();
                String tipo = partes[1].trim();
                double valor = Double.parseDouble(partes[2].trim());
                String fecha = partes[3].trim();

                Object[] fila = new Object[]{codigo, tipo, valor, fecha};

                modeloTabla.addRow(fila);  
                CargaMasiva.add(fila);    
            }

            JOptionPane.showMessageDialog(this, "Archivo cargado correctamente.");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al leer el archivo: " + ex.getMessage());
        }}
    }//GEN-LAST:event_jButton4ActionPerformed

   
    

