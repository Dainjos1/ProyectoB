    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if (CargaMasiva.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay cupones que agregar.");
            return;
        }

        for (Object[] datos : CargaMasiva) {
            String codigo = (String) datos[0];
            String Tipo = (String) datos[1];
            double valor = (double) datos[2];
            String fechaVencimiento = (String) datos[3];

            Cupones cupon = new Cupones(codigo, Tipo, valor, fechaVencimiento);
            Proyectob.cupones.add(cupon);

        }

        CargaMasiva.clear();
        modeloTabla.setRowCount(0);
        JOptionPane.showMessageDialog(this, "Cupon guardado exitosamente.");
    
        
    }//GEN-LAST:event_jButton1ActionPerformed

