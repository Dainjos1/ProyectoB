        private void pintarTabla(){
        String cabeceras[] = {"Codigo","Tipo","Valor","Fecha de Vencimiento"};
        
        DefaultTableModel t = new DefaultTableModel(cabeceras,Proyectob.cupones.size());
        jTable1.setModel(t);

        TableModel tabla = jTable1.getModel();
        
        for (int i = 0; i < Proyectob.cupones.size();i++){
          Cupones c = Proyectob.cupones.get(i);
          tabla.setValueAt(c.getCodigo(),i,0);
          tabla.setValueAt(c.getTipo(),i,1);
          tabla.setValueAt(c.getValor(),i,2);
          tabla.setValueAt(c.getFechaVencimiento(),i,3);
          
        
    }
        }
