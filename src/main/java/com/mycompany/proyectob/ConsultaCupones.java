    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int borrar = jTable1.getSelectedRow();
        if(borrar>-1){
            
                if(JOptionPane.showConfirmDialog(this,"Esta seguro de eliminar el cupon")==0){
            
                Proyectob.cupones.remove(borrar);
                pintarTabla();
                JOptionPane.showMessageDialog(this,"Cupon eliminado");
                }
            
}
        else{
            JOptionPane.showMessageDialog(this,"Seleccione un cupon a borrar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int modificar = jTable1.getSelectedRow();
        if (modificar>-1){
            cupones = Proyectob.cupones.get(modificar);
            
            jTextField1.setText(String.valueOf(cupones.getCodigo()));
            
            jComboBox1.setSelectedItem(cupones.getTipo());

            
            jTextField2.setText(String.valueOf(cupones.getValor()));
            
            jTextField3.setText(cupones.getFechaVencimiento());
            

        }
        else{
                        JOptionPane.showMessageDialog(this,"Seleccione un elemento a modificar");

        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (cupones!=null){
            
             try {
            String codigo = (jTextField1.getText());
            cupones.setCodigo(codigo);
            } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El codigo debe ser un número válido.");
        }
          
            }
             

             try {
            double valor = Double.parseDouble(jTextField2.getText());
            cupones.setValor(valor);
            
            } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El precio debe ser un número válido.");
        }

        cupones.setFechaVencimiento(jTextField3.getText());
        
        cupones.setTipo(jComboBox1.getSelectedItem().toString());
                pintarTabla();

                
    }//GEN-LAST:event_jButton4ActionPerformed

