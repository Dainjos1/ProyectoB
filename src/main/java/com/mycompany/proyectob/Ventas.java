    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:\
String nit = jTextField2.getText();
        String nombre = jTextField3.getText();
        String direccion = jTextField4.getText();

        if (carrito.isEmpty()) {
            JOptionPane.showMessageDialog(this, "No hay libros en la venta.");
            return;
        }

        for (Object[] item : carrito) {
            Libros libro = (Libros) item[0];
            int cantidad = (int) item[1];

            Venta venta = new Venta(usuarioActual.getNombre(), libro, cantidad, nit, nombre, direccion);
            Proyectob.ventas.add(venta);

            libro.setStock(libro.getStock() - cantidad);
        }

        carrito.clear();
        modeloTabla.setRowCount(0);
jTextField5.setText("");
        JOptionPane.showMessageDialog(this, "Venta registrada exitosamente.");
    
    
    }//GEN-LAST:event_jButton1ActionPerformed

