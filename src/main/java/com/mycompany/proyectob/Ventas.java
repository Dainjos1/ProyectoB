    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String titulo = (String) jComboBox1.getSelectedItem();
        int cantidad;

        try {
            cantidad = Integer.parseInt(jTextField1.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Cantidad invÃ¡lida.");
            return;
        }

        Libros libro = null;
        for (Libros l : Proyectob.libros) {
            if (l.getTitulo().equals(titulo)) {
                libro = l;
                break;
            }
        }

        if (libro == null || libro.getStock() < cantidad) {
            JOptionPane.showMessageDialog(this, "Stock insuficiente o libro no encontrado.");
            return;
        }

        double precio = libro.getPrecio();
        double subtotal = precio * cantidad;

        carrito.add(new Object[]{libro, cantidad});
        modeloTabla.addRow(new Object[]{titulo, cantidad, String.format("Q%.2f", precio), String.format("Q%.2f", subtotal)});

        double total = calcularTotal();
        jTextField5.setText(String.format("Q%.2f", total)); 
        
        }

    private double calcularTotal() {
        double total = 0;
        for (Object[] item : carrito) {
            Libros libro = (Libros) item[0];
            int cantidad = (int) item[1];
            total += libro.getPrecio() * cantidad;
        }
        return total;
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

    

