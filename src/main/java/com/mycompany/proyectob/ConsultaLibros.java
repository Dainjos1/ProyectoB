    private void pintarTabla(){
        String cabeceras[] = {"Titulo","Autor","Genero","Precio","Stock"};
        
        DefaultTableModel t = new DefaultTableModel(cabeceras,Proyectob.libros.size());
        jTable1.setModel(t);

        TableModel tabla = jTable1.getModel();
        
        for (int i = 0; i < Proyectob.libros.size();i++){
          Libros l = Proyectob.libros.get(i);
          tabla.setValueAt(l.getTitulo(),i,0);
          tabla.setValueAt(l.getAutor(),i,1);
          tabla.setValueAt(l.getGenero(),i,2);
          tabla.setValueAt(l.getPrecio(),i,3);
          tabla.setValueAt(l.getStock(),i,4);
        }   
    }

