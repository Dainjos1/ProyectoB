    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int borrar = jTable1.getSelectedRow();
        if(borrar>-1){
            
                if(JOptionPane.showConfirmDialog(this,"Esta seguro de eliminar el libro")==0){
            
                Proyectob.libros.remove(borrar);
                pintarTabla();
                JOptionPane.showMessageDialog(this,"Libro eliminado");
                }
            
}
        else{
            JOptionPane.showMessageDialog(this,"Selecciones un libro a borrar");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int modificar = jTable1.getSelectedRow();
        if (modificar>-1){
            libro = Proyectob.libros.get(modificar);
            jTextField1.setText(libro.getTitulo());
            jTextField2.setText(libro.getAutor());
            jComboBox1.setSelectedItem(libro.getGenero());
            jTextField3.setText(String.valueOf(libro.getPrecio()));
            jTextField4.setText(String.valueOf(libro.getStock()));
           
            
        
            

        }
        else{
                        JOptionPane.showMessageDialog(this,"Seleccione un elemento a modificar");

        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
                if (libro!=null){

        libro.setTitulo(jTextField1.getText());
        libro.setAutor(jTextField2.getText());
        libro.setGenero(jComboBox1.getSelectedItem().toString());
       
        

        try {
            
            double precio = Double.parseDouble(jTextField3.getText());
            int stock = Integer.parseInt(jTextField4.getText());
            
             libro.setPrecio(precio);
             libro.setStock(stock);
        
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "El precio debe ser un número válido.");
        }
                pintarTabla();

                }
    }//GEN-LAST:event_jButton4ActionPerformed


